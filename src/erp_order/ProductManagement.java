    private void addProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProductButtonActionPerformed

        if (!productNameField.getText().equals("") && !productCategoryField.getText().equals("")) {

            DefaultTableModel model = (DefaultTableModel) productMaterialListTable.getModel();
            int rowNum = model.getRowCount();
            for (int i = 0; i < rowNum; i++) {

                ProductReq pr = new ProductReq(productNameField.getText(), productCategoryField.getText(),
                        (String) model.getValueAt(i, 0), (String) model.getValueAt(i, 1), Double.parseDouble((String)model.getValueAt(i, 2)));

                try {
                    if (Database.insertProduct(pr)) {
                       ((DefaultTableModel) jTable1.getModel()).addRow(new Object[]{productNameField.getText(), productCategoryField.getText(),
                        (String) model.getValueAt(i, 0), (String) model.getValueAt(i, 1),
                        Double.parseDouble((String)model.getValueAt(i, 2))});
                    }
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ProductManagement.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                    Logger.getLogger(ProductManagement.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            while (model.getRowCount() != 0) {
                model.removeRow(0);
            }

        }

    }//GEN-LAST:event_addProductButtonActionPerformed


