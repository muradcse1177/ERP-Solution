    private void adMaterialButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adMaterialButtonActionPerformed
        if (!materialTypeCombo.getSelectedItem().toString().equals("Select Material Type")
                && !materialSubTypeCombo.getSelectedItem().toString().equals("Select Material Sub Type")
                && !reqQtyField.getText().equals("")) {
            
            try{
            double d = Double.parseDouble(reqQtyField.getText());
            }catch(NumberFormatException e)
            {
                return;
            }
            DefaultTableModel model = (DefaultTableModel) productMaterialListTable.getModel();
            int rowCnt = model.getRowCount();
            int fr = -1;
            for(int i=0; i<rowCnt; i++)
            {
                if(((String)model.getValueAt(i, 0)).equals(materialTypeCombo.getSelectedItem().toString())&&
                        ((String)model.getValueAt(i, 1)).equals(materialSubTypeCombo.getSelectedItem().toString())){
                    fr = i;
                    break;
                }
            }
            if(fr==-1)
            model.addRow(new Object[]{materialTypeCombo.getSelectedItem().toString(),
                materialSubTypeCombo.getSelectedItem().toString(),
                reqQtyField.getText()});
            else
            {
                model.setValueAt(reqQtyField.getText(), fr, 2);
            }
        }
    }//GEN-LAST:event_adMaterialButtonActionPerformed

