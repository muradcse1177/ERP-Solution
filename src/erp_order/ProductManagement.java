    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getSelectedRow() != -1) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            if (model.getValueAt(jTable1.getSelectedRow(), 0) != null && model.getValueAt(jTable1.getSelectedRow(), 1) != null &&
                    model.getValueAt(jTable1.getSelectedRow(), 2) != null && model.getValueAt(jTable1.getSelectedRow(), 3) != null) {
                
                    ProductReq req = new ProductReq((String)model.getValueAt(jTable1.getSelectedRow(), 0),
                            (String)model.getValueAt(jTable1.getSelectedRow(), 1),
                            (String)model.getValueAt(jTable1.getSelectedRow(), 2), 
                            (String)model.getValueAt(jTable1.getSelectedRow(), 3), (double) model.getValueAt(jTable1.getSelectedRow(), 4));
                try {
                    if (Database.removeProductRequirement(req)) {
                        model.removeRow(jTable1.getSelectedRow());
                    } else {
                        JOptionPane.showMessageDialog(ProductManagement.this, "No such Item found.");
                    }
                } catch (Exception ex) {
                    Logger.getLogger(ProductManagement.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed
