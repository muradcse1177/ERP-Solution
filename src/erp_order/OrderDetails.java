    private void imageLblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imageLblMouseClicked
        JFileChooser chooser = new JFileChooser();
        if(chooser.showOpenDialog(OrderDetails.this)==JFileChooser.APPROVE_OPTION){
            File f = chooser.getSelectedFile();
            try {
                imageUrl = f.getAbsolutePath();
                BufferedImage img =  Scalr.resize(ImageIO.read(new File(imageUrl)), Method.AUTOMATIC, Scalr.Mode.AUTOMATIC, 246, 185, Scalr.OP_ANTIALIAS);
                imageLbl.setText("");
                imageLbl.setIcon(new ImageIcon(img));
            } catch (IOException ex) {
                Logger.getLogger(OrderDetails.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            
        }
    }//GEN-LAST:event_imageLblMouseClicked

