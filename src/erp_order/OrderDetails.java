    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        
        if(!finishedProductCombo.getSelectedItem().toString().equals("Select Finished Product")
                &&!productCategoryCombo.getSelectedItem().toString().equals("Select Product Category ")
                &&!clientName.getText().equals("")
                &&!orderDateField.getText().equals("")
                &&!delivaryDateField.getText().equals("")
                &&!quantityField.getText().equals("")){
            OrderDetail ord = new OrderDetail();
            ord.setId(Database.nextOrderId());
            ord.setClientName(clientName.getText());
            ord.setAddress(addressField.getText());
            ord.setOrderDate(orderDateField.getText());
            ord.setDelevaryDate(delivaryDateField.getText());
            ord.setFinishedProduct(finishedProductCombo.getSelectedItem().toString());
            ord.setProductCategory(productCategoryCombo.getSelectedItem().toString());
            ord.setDesighUrl(imageUrl);
            ord.setQuantity(Double.parseDouble(quantityField.getText()));
            ord.setCompleted(Boolean.FALSE);
            
            try {            
                Database.saveOrder(ord);
            } catch (Exception ex) {
                Logger.getLogger(OrderDetails.class.getName()).log(Level.SEVERE, null, ex);
            }
            OrderDetails.this.dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

